"""
Segmentation training script for Falcon desert hackathon dataset.

Upgrades included:
- Correct default class mapping for Falcon class IDs
- Joint image/mask augmentations
- Optional auto class weighting for imbalance
- Mixed precision support (CUDA)
- Learning-rate scheduler + early stopping
- Best-checkpoint export and richer training artifacts
"""

import argparse
import csv
import json
import os
import random
from pathlib import Path

import matplotlib.pyplot as plt
import numpy as np
import torch
import torch.nn.functional as F
import torchvision.transforms as transforms
from PIL import Image, ImageOps
from torch import nn
from torch.optim import AdamW, SGD
from torch.optim.lr_scheduler import CosineAnnealingLR
from torch.utils.data import DataLoader, Dataset
from tqdm import tqdm


plt.switch_backend("Agg")

IMAGE_EXTS = {".png", ".jpg", ".jpeg", ".bmp", ".tif", ".tiff"}
IGNORE_INDEX = 255

DEFAULT_VALUE_MAP = {
    100: 0,   # Trees
    200: 1,   # Lush Bushes
    300: 2,   # Dry Grass
    500: 3,   # Dry Bushes
    550: 4,   # Ground Clutter
    600: 5,   # Flowers
    700: 6,   # Logs
    800: 7,   # Rocks
    7100: 8,  # Landscape
    10000: 9, # Sky
}

DEFAULT_CLASS_NAMES = [
    "Trees",
    "Lush Bushes",
    "Dry Grass",
    "Dry Bushes",
    "Ground Clutter",
    "Flowers",
    "Logs",
    "Rocks",
    "Landscape",
    "Sky",
]

value_map = DEFAULT_VALUE_MAP.copy()
mapping_mode = "int"
n_classes = (max(value_map.values()) + 1) if value_map else 0
class_names = DEFAULT_CLASS_NAMES.copy()


def set_seed(seed: int) -> None:
    random.seed(seed)
    np.random.seed(seed)
    torch.manual_seed(seed)
    torch.cuda.manual_seed_all(seed)
    torch.backends.cudnn.deterministic = True
    torch.backends.cudnn.benchmark = False


def _ensure_contiguous_mapping(parsed: dict) -> None:
    values = sorted(set(parsed.values()))
    expected = list(range(len(values)))
    if values != expected:
        raise ValueError(
            "Mapping class IDs must be contiguous and start at 0. "
            f"Found IDs: {values}"
        )


def load_value_map(mapping_json_path: str):
    """Load mask value mapping from JSON generated by generate_mask_mapping.py."""
    with open(mapping_json_path, "r", encoding="utf-8") as f:
        data = json.load(f)

    mode = data.get("mode", "int")
    raw_mapping = data.get("mapping", {})
    parsed = {}

    if mode == "rgb":
        for key, value in raw_mapping.items():
            rgb = tuple(int(x) for x in str(key).split(","))
            if len(rgb) != 3:
                raise ValueError(f"Invalid RGB key in mapping: {key}")
            parsed[rgb] = int(value)
    else:
        for key, value in raw_mapping.items():
            parsed[int(key)] = int(value)

    if not parsed:
        raise ValueError("Loaded mapping is empty")

    _ensure_contiguous_mapping(parsed)
    return parsed, mode


def infer_class_names_from_map(current_map: dict) -> list:
    if all(isinstance(k, int) for k in current_map.keys()):
        if set(current_map.keys()) == set(DEFAULT_VALUE_MAP.keys()):
            return DEFAULT_CLASS_NAMES.copy()
    return [f"Class_{idx}" for idx in range(max(current_map.values()) + 1)]


def set_value_map(new_map, mode):
    """Update global mapping settings used by mask conversion."""
    global value_map, n_classes, mapping_mode, class_names
    value_map = new_map
    mapping_mode = mode
    n_classes = (max(value_map.values()) + 1) if value_map else 0
    class_names = infer_class_names_from_map(value_map)


def convert_mask(mask: Image.Image) -> Image.Image:
    """Convert raw mask values to contiguous class IDs, unknown -> IGNORE_INDEX."""
    arr = np.array(mask)

    if mapping_mode == "rgb":
        if arr.ndim == 2:
            arr = np.stack([arr, arr, arr], axis=-1)
        if arr.shape[2] > 3:
            arr = arr[:, :, :3]

        new_arr = np.full(arr.shape[:2], IGNORE_INDEX, dtype=np.uint8)
        for raw_value, new_value in value_map.items():
            rgb = np.array(raw_value, dtype=arr.dtype)
            new_arr[np.all(arr == rgb, axis=-1)] = new_value
    else:
        if arr.ndim == 3:
            arr = arr[:, :, 0]
        new_arr = np.full(arr.shape, IGNORE_INDEX, dtype=np.uint8)
        for raw_value, new_value in value_map.items():
            new_arr[arr == raw_value] = new_value

    return Image.fromarray(new_arr)


class JointAugment:
    def __init__(self, hflip: float, vflip: float, jitter_strength: float):
        self.hflip = hflip
        self.vflip = vflip
        if jitter_strength > 0:
            self.jitter = transforms.ColorJitter(
                brightness=jitter_strength,
                contrast=jitter_strength,
                saturation=jitter_strength,
                hue=min(0.1, jitter_strength / 2.0),
            )
        else:
            self.jitter = None

    def __call__(self, image: Image.Image, mask: Image.Image):
        if self.hflip > 0 and random.random() < self.hflip:
            image = ImageOps.mirror(image)
            mask = ImageOps.mirror(mask)

        if self.vflip > 0 and random.random() < self.vflip:
            image = ImageOps.flip(image)
            mask = ImageOps.flip(mask)

        if self.jitter is not None:
            image = self.jitter(image)

        return image, mask


class MaskDataset(Dataset):
    def __init__(
        self,
        data_dir,
        image_transform,
        mask_resize,
        augment=None,
    ):
        self.root = Path(data_dir)
        self.image_dir = self.root / "Color_Images"
        self.mask_dir = self.root / "Segmentation"
        self.image_transform = image_transform
        self.mask_resize = mask_resize
        self.augment = augment

        if not self.image_dir.exists():
            raise FileNotFoundError(f"Missing folder: {self.image_dir}")
        if not self.mask_dir.exists():
            raise FileNotFoundError(f"Missing folder: {self.mask_dir}")

        image_names = {
            p.name for p in self.image_dir.iterdir() if p.is_file() and p.suffix.lower() in IMAGE_EXTS
        }
        mask_names = {
            p.name for p in self.mask_dir.iterdir() if p.is_file() and p.suffix.lower() in IMAGE_EXTS
        }
        self.data_ids = sorted(image_names & mask_names)

        if not self.data_ids:
            raise RuntimeError(f"No matching image/mask file names in {self.root}")

    def __len__(self):
        return len(self.data_ids)

    def __getitem__(self, idx):
        name = self.data_ids[idx]
        image = Image.open(self.image_dir / name).convert("RGB")
        mask = Image.open(self.mask_dir / name)
        mask = convert_mask(mask)

        if self.augment is not None:
            image, mask = self.augment(image, mask)

        image = self.image_transform(image)
        mask = self.mask_resize(mask)
        mask_arr = np.array(mask, dtype=np.uint8)
        mask_tensor = torch.from_numpy(mask_arr.astype(np.int64))

        return image, mask_tensor


class SegmentationHeadConvNeXt(nn.Module):
    def __init__(self, in_channels, out_channels, token_w, token_h):
        super().__init__()
        self.h = token_h
        self.w = token_w

        self.stem = nn.Sequential(
            nn.Conv2d(in_channels, 128, kernel_size=7, padding=3),
            nn.GELU(),
        )

        self.block = nn.Sequential(
            nn.Conv2d(128, 128, kernel_size=7, padding=3, groups=128),
            nn.GELU(),
            nn.Conv2d(128, 128, kernel_size=1),
            nn.GELU(),
        )

        self.classifier = nn.Conv2d(128, out_channels, kernel_size=1)

    def forward(self, x):
        bsz, n_tokens, channels = x.shape
        x = x.reshape(bsz, self.h, self.w, channels).permute(0, 3, 1, 2)
        x = self.stem(x)
        x = self.block(x)
        return self.classifier(x)


def update_confusion_matrix(conf_mat, pred, target, num_classes, ignore_index):
    valid = target != ignore_index
    if valid.sum() == 0:
        return conf_mat

    pred = pred[valid]
    target = target[valid]
    idx = target * num_classes + pred
    bins = torch.bincount(idx, minlength=num_classes * num_classes)
    conf_mat += bins.reshape(num_classes, num_classes).cpu().numpy().astype(np.int64)
    return conf_mat


def metrics_from_confusion(conf_mat):
    conf = conf_mat.astype(np.float64)
    tp = np.diag(conf)
    fp = conf.sum(axis=0) - tp
    fn = conf.sum(axis=1) - tp
    union = tp + fp + fn

    iou = np.divide(tp, union, out=np.full_like(tp, np.nan), where=union > 0)
    dice_den = (2.0 * tp) + fp + fn
    dice = np.divide(2.0 * tp, dice_den, out=np.full_like(tp, np.nan), where=dice_den > 0)

    total = conf.sum()
    pixel_acc = float(tp.sum() / total) if total > 0 else 0.0

    return {
        "mean_iou": float(np.nanmean(iou)),
        "mean_dice": float(np.nanmean(dice)),
        "pixel_acc": pixel_acc,
        "per_class_iou": iou.tolist(),
        "per_class_dice": dice.tolist(),
    }


def build_class_weights(mask_dir: Path, max_masks: int = 0):
    mask_paths = [
        p for p in sorted(mask_dir.iterdir()) if p.is_file() and p.suffix.lower() in IMAGE_EXTS
    ]
    if not mask_paths:
        raise RuntimeError(f"No masks found in {mask_dir}")

    if max_masks > 0:
        mask_paths = mask_paths[:max_masks]

    counts = np.zeros(n_classes, dtype=np.float64)
    for path in tqdm(mask_paths, desc="Class weights", leave=False):
        mask = convert_mask(Image.open(path))
        arr = np.array(mask, dtype=np.uint8)
        valid = arr != IGNORE_INDEX
        if np.any(valid):
            binc = np.bincount(arr[valid].astype(np.int64), minlength=n_classes)
            counts += binc

    counts[counts == 0] = 1.0
    inv_freq = counts.sum() / (n_classes * counts)
    inv_freq = inv_freq / np.mean(inv_freq)
    return torch.tensor(inv_freq.astype(np.float32))


def run_epoch(
    classifier,
    backbone,
    loader,
    optimizer,
    scaler,
    loss_fn,
    device,
    use_amp,
    train_mode,
):
    classifier.train(train_mode)
    conf_mat = np.zeros((n_classes, n_classes), dtype=np.int64)
    losses = []

    desc = "Train" if train_mode else "Val"
    loop = tqdm(loader, desc=desc, leave=False)

    for imgs, labels in loop:
        imgs = imgs.to(device, non_blocking=True)
        labels = labels.to(device, non_blocking=True)

        if train_mode:
            optimizer.zero_grad(set_to_none=True)

        with torch.no_grad():
            feats = backbone.forward_features(imgs)["x_norm_patchtokens"]

        with torch.cuda.amp.autocast(enabled=use_amp):
            logits = classifier(feats)
            logits = F.interpolate(
                logits,
                size=imgs.shape[2:],
                mode="bilinear",
                align_corners=False,
            )
            loss = loss_fn(logits, labels)

        if train_mode:
            scaler.scale(loss).backward()
            scaler.step(optimizer)
            scaler.update()

        preds = torch.argmax(logits.detach(), dim=1)
        update_confusion_matrix(
            conf_mat,
            pred=preds.cpu(),
            target=labels.detach().cpu(),
            num_classes=n_classes,
            ignore_index=IGNORE_INDEX,
        )

        loss_value = float(loss.item())
        losses.append(loss_value)
        loop.set_postfix(loss=f"{loss_value:.4f}")

    metrics = metrics_from_confusion(conf_mat)
    mean_loss = float(np.mean(losses)) if losses else 0.0
    return mean_loss, metrics


