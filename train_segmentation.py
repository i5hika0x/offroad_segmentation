def save_history(history: dict, output_dir: Path, best_epoch: int) -> None:
    output_dir.mkdir(parents=True, exist_ok=True)

    csv_path = output_dir / "history.csv"
    with csv_path.open("w", newline="", encoding="utf-8") as f:
        writer = csv.writer(f)
        writer.writerow([
            "epoch",
            "lr",
            "train_loss",
            "val_loss",
            "train_iou",
            "val_iou",
            "train_dice",
            "val_dice",
            "train_pixel_acc",
            "val_pixel_acc",
        ])
        for i in range(len(history["epoch"])):
            writer.writerow([
                history["epoch"][i],
                history["lr"][i],
                history["train_loss"][i],
                history["val_loss"][i],
                history["train_iou"][i],
                history["val_iou"][i],
                history["train_dice"][i],
                history["val_dice"][i],
                history["train_pixel_acc"][i],
                history["val_pixel_acc"][i],
            ])

    payload = {
        "best_epoch": best_epoch,
        "history": history,
    }
    with (output_dir / "history.json").open("w", encoding="utf-8") as f:
        json.dump(payload, f, indent=2)


def parse_args():
    script_dir = Path(__file__).resolve().parent

    parser = argparse.ArgumentParser(description="Train segmentation model")
    parser.add_argument(
        "--train_dir",
        type=str,
        default=str(script_dir / ".." / "Offroad_Segmentation_Training_Dataset" / "train"),
    )
    parser.add_argument(
        "--val_dir",
        type=str,
        default=str(script_dir / ".." / "Offroad_Segmentation_Training_Dataset" / "val"),
    )
    parser.add_argument("--output_dir", type=str, default=str(script_dir / "train_stats"))
    parser.add_argument(
        "--mapping_json",
        type=str,
        default="",
        help="Optional mapping JSON generated by generate_mask_mapping.py",
    )

    parser.add_argument("--batch_size", type=int, default=2)
    parser.add_argument("--epochs", type=int, default=20)
    parser.add_argument("--lr", type=float, default=3e-4)
    parser.add_argument("--weight_decay", type=float, default=1e-4)
    parser.add_argument("--num_workers", type=int, default=0)
    parser.add_argument("--seed", type=int, default=42)

    parser.add_argument(
        "--backbone_size",
        type=str,
        default="small",
        choices=["small", "base", "large", "giant"],
    )
    parser.add_argument("--optimizer", type=str, default="adamw", choices=["adamw", "sgd"])
    parser.add_argument("--scheduler", type=str, default="cosine", choices=["cosine", "none"])
    parser.add_argument("--early_stop_patience", type=int, default=8)
    parser.add_argument("--amp", type=int, default=1, choices=[0, 1])

    parser.add_argument(
        "--class_weighting",
        type=str,
        default="auto",
        choices=["auto", "none"],
    )
    parser.add_argument(
        "--max_weight_masks",
        type=int,
        default=0,
        help="0 means use all train masks while estimating class weights",
    )

    parser.add_argument("--aug_hflip", type=float, default=0.5)
    parser.add_argument("--aug_vflip", type=float, default=0.0)
    parser.add_argument("--aug_color_jitter", type=float, default=0.2)
    parser.add_argument("--disable_augmentation", action="store_true")

    return parser.parse_args()


def main():
    args = parse_args()
    set_seed(args.seed)

    if args.mapping_json:
        loaded_map, loaded_mode = load_value_map(args.mapping_json)
        set_value_map(loaded_map, loaded_mode)
        print(f"Loaded mapping from {args.mapping_json} | mode={mapping_mode} | classes={n_classes}")
    else:
        print(f"Using default Falcon mapping | mode={mapping_mode} | classes={n_classes}")

    device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
    use_amp = bool(args.amp) and device.type == "cuda"

    print(f"Using device: {device}")
    print(f"AMP enabled: {use_amp}")

    output_dir = Path(args.output_dir)
    output_dir.mkdir(parents=True, exist_ok=True)
    checkpoint_dir = output_dir / "checkpoints"
    checkpoint_dir.mkdir(parents=True, exist_ok=True)

    w = int(((960 / 2) // 14) * 14)
    h = int(((540 / 2) // 14) * 14)

    image_transform = transforms.Compose([
        transforms.Resize((h, w)),
        transforms.ToTensor(),
        transforms.Normalize(mean=[0.485, 0.456, 0.406], std=[0.229, 0.224, 0.225]),
    ])
    mask_resize = transforms.Resize((h, w), interpolation=transforms.InterpolationMode.NEAREST)

    augment = None
    if not args.disable_augmentation:
        augment = JointAugment(
            hflip=args.aug_hflip,
            vflip=args.aug_vflip,
            jitter_strength=args.aug_color_jitter,
        )

    print(f"Train dir: {args.train_dir}")
    print(f"Val dir:   {args.val_dir}")

    trainset = MaskDataset(
        data_dir=args.train_dir,
        image_transform=image_transform,
        mask_resize=mask_resize,
        augment=augment,
    )
    valset = MaskDataset(
        data_dir=args.val_dir,
        image_transform=image_transform,
        mask_resize=mask_resize,
        augment=None,
    )

    pin_memory = device.type == "cuda"
    train_loader = DataLoader(
        trainset,
        batch_size=args.batch_size,
        shuffle=True,
        num_workers=args.num_workers,
        pin_memory=pin_memory,
    )
    val_loader = DataLoader(
        valset,
        batch_size=args.batch_size,
        shuffle=False,
        num_workers=args.num_workers,
        pin_memory=pin_memory,
    )

    print(f"Training samples: {len(trainset)}")
    print(f"Validation samples: {len(valset)}")

    print("Loading DINOv2 backbone...")
    backbone_archs = {
        "small": "vits14",
        "base": "vitb14_reg",
        "large": "vitl14_reg",
        "giant": "vitg14_reg",
    }
    backbone_name = f"dinov2_{backbone_archs[args.backbone_size]}"
    backbone = torch.hub.load("facebookresearch/dinov2", backbone_name)
    backbone.to(device).eval()
    print(f"Backbone loaded: {backbone_name}")

    sample_imgs, _ = next(iter(train_loader))
    sample_imgs = sample_imgs.to(device)
    with torch.no_grad():
        emb = backbone.forward_features(sample_imgs)["x_norm_patchtokens"]
    n_embedding = emb.shape[2]
    print(f"Embedding dimension: {n_embedding}")

    classifier = SegmentationHeadConvNeXt(
        in_channels=n_embedding,
        out_channels=n_classes,
        token_w=w // 14,
        token_h=h // 14,
    ).to(device)

    class_weights = None
    if args.class_weighting == "auto":
        class_weights = build_class_weights(
            mask_dir=Path(args.train_dir) / "Segmentation",
            max_masks=args.max_weight_masks,
        ).to(device)
        print("Auto class weights:", class_weights.detach().cpu().numpy().round(3).tolist())

    loss_fn = nn.CrossEntropyLoss(weight=class_weights, ignore_index=IGNORE_INDEX)

    if args.optimizer == "adamw":
        optimizer = AdamW(classifier.parameters(), lr=args.lr, weight_decay=args.weight_decay)
    else:
        optimizer = SGD(classifier.parameters(), lr=args.lr, momentum=0.9, weight_decay=args.weight_decay)

    scheduler = None
    if args.scheduler == "cosine":
        scheduler = CosineAnnealingLR(optimizer, T_max=args.epochs)

    scaler = torch.cuda.amp.GradScaler(enabled=use_amp)

    history = {
        "epoch": [],
        "lr": [],
        "train_loss": [],
        "val_loss": [],
        "train_iou": [],
        "val_iou": [],
        "train_dice": [],
        "val_dice": [],
        "train_pixel_acc": [],
        "val_pixel_acc": [],
    }

    best_val_iou = -1.0
    best_epoch = 0
    epochs_without_improve = 0
    best_path = checkpoint_dir / "best_segmentation_head.pth"
    last_path = checkpoint_dir / "last_segmentation_head.pth"

    print("\nStarting training...")
    print("=" * 80)

    for epoch in range(1, args.epochs + 1):
        train_loss, train_metrics = run_epoch(
            classifier=classifier,
            backbone=backbone,
            loader=train_loader,
            optimizer=optimizer,
            scaler=scaler,
            loss_fn=loss_fn,
            device=device,
            use_amp=use_amp,
            train_mode=True,
        )

        with torch.no_grad():
            val_loss, val_metrics = run_epoch(
                classifier=classifier,
                backbone=backbone,
                loader=val_loader,
                optimizer=optimizer,
                scaler=scaler,
                loss_fn=loss_fn,
                device=device,
                use_amp=use_amp,
                train_mode=False,
            )

        if scheduler is not None:
            scheduler.step()

        lr_now = float(optimizer.param_groups[0]["lr"])

        history["epoch"].append(epoch)
        history["lr"].append(lr_now)
        history["train_loss"].append(train_loss)
        history["val_loss"].append(val_loss)
        history["train_iou"].append(train_metrics["mean_iou"])
        history["val_iou"].append(val_metrics["mean_iou"])
        history["train_dice"].append(train_metrics["mean_dice"])
        history["val_dice"].append(val_metrics["mean_dice"])
        history["train_pixel_acc"].append(train_metrics["pixel_acc"])
        history["val_pixel_acc"].append(val_metrics["pixel_acc"])

        print(
            f"Epoch {epoch:03d}/{args.epochs:03d} | "
            f"train_loss={train_loss:.4f} val_loss={val_loss:.4f} | "
            f"val_iou={val_metrics['mean_iou']:.4f} val_dice={val_metrics['mean_dice']:.4f} "
            f"val_acc={val_metrics['pixel_acc']:.4f} lr={lr_now:.6f}"
        )

        torch.save(classifier.state_dict(), last_path)

        improved = val_metrics["mean_iou"] > best_val_iou
        if improved:
            best_val_iou = val_metrics["mean_iou"]
            best_epoch = epoch
            epochs_without_improve = 0
            torch.save(classifier.state_dict(), best_path)
        else:
            epochs_without_improve += 1

        if args.early_stop_patience > 0 and epochs_without_improve >= args.early_stop_patience:
            print(
                "Early stopping triggered: "
                f"no val IoU improvement for {args.early_stop_patience} epochs."
            )
            break

    save_training_plots(history, output_dir)
    save_history(history, output_dir, best_epoch=best_epoch)

    if best_path.exists():
        best_state = torch.load(best_path, map_location="cpu")
    else:
        best_state = classifier.state_dict()

    script_dir = Path(__file__).resolve().parent
    final_export_path = script_dir / "segmentation_head.pth"
    torch.save(best_state, final_export_path)

    class_names_path = output_dir / "class_names.json"
    with class_names_path.open("w", encoding="utf-8") as f:
        json.dump(class_names, f, indent=2)

    class_names_root_path = script_dir / "class_names.json"
    with class_names_root_path.open("w", encoding="utf-8") as f:
        json.dump(class_names, f, indent=2)

    summary = {
        "best_epoch": best_epoch,
        "best_val_iou": best_val_iou,
        "epochs_ran": len(history["epoch"]),
        "model_export": str(final_export_path),
        "best_checkpoint": str(best_path),
        "last_checkpoint": str(last_path),
        "class_names_output": str(class_names_path),
        "class_names_root": str(class_names_root_path),
    }
    with (output_dir / "training_summary.json").open("w", encoding="utf-8") as f:
        json.dump(summary, f, indent=2)

    print("\nTraining complete!")
    print(f"Best epoch: {best_epoch}")
    print(f"Best val IoU: {best_val_iou:.4f}")
    print(f"Best checkpoint: {best_path}")
    print(f"Exported model for inference: {final_export_path}")


if __name__ == "__main__":
    main()
