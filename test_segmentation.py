def main():
    script_dir = os.path.dirname(os.path.abspath(__file__))

    parser = argparse.ArgumentParser()
    parser.add_argument('--model_path', type=str, default=os.path.join(script_dir, 'segmentation_head.pth'))
    parser.add_argument('--data_dir', type=str, default=os.path.join(script_dir, '..', 'Offroad_Segmentation_testImages'))
    parser.add_argument('--output_dir', type=str, default='./predictions')
    parser.add_argument('--batch_size', type=int, default=2)
    parser.add_argument('--num_workers', type=int, default=0)
    parser.add_argument('--mapping_json', type=str, default='', help='Optional mapping JSON generated by generate_mask_mapping.py')
    args = parser.parse_args()

    if args.mapping_json:
        loaded_map, loaded_mode = load_value_map(args.mapping_json)
        set_value_map(loaded_map, loaded_mode)
        print(f"Loaded mapping from {args.mapping_json} | mode={mapping_mode} | classes={n_classes}")
    else:
        print(f"Using default mapping | mode={mapping_mode} | classes={n_classes}")

    os.makedirs(args.output_dir, exist_ok=True)

    device = torch.device('cuda' if torch.cuda.is_available() else 'cpu')
    print(f"Using device: {device}")

    w = int(((960 / 2) // 14) * 14)
    h = int(((540 / 2) // 14) * 14)

    transform = transforms.Compose([
        transforms.Resize((h, w)),
        transforms.ToTensor(),
        transforms.Normalize([0.485, 0.456, 0.406], [0.229, 0.224, 0.225])
    ])

    mask_transform = transforms.Compose([
        transforms.Resize((h, w), interpolation=transforms.InterpolationMode.NEAREST),
        transforms.ToTensor(),
    ])

    valset = MaskDataset(args.data_dir, transform, mask_transform)
    val_loader = DataLoader(valset, batch_size=args.batch_size, shuffle=False, num_workers=args.num_workers)

    backbone = torch.hub.load("facebookresearch/dinov2", "dinov2_vits14").to(device).eval()

    sample_img, _, _ = valset[0]
    with torch.no_grad():
        embed = backbone.forward_features(sample_img.unsqueeze(0).to(device))["x_norm_patchtokens"]
    n_embed = embed.shape[2]

    classifier = SegmentationHeadConvNeXt(
        n_embed, n_classes, w // 14, h // 14
    ).to(device)
    classifier.load_state_dict(torch.load(args.model_path, map_location=device))
    classifier.eval()

    iou_scores, dice_scores, pixel_accs = [], [], []
    map50_scores, all_class_map50 = [], []

    with torch.no_grad():
        for imgs, labels, _ in tqdm(val_loader):
            imgs, labels = imgs.to(device), labels.to(device)
            feats = backbone.forward_features(imgs)["x_norm_patchtokens"]
            logits = classifier(feats)
            outputs = F.interpolate(logits, size=imgs.shape[2:], mode="bilinear", align_corners=False)

            labels = labels.squeeze(1).long()

            iou, _ = compute_iou(outputs, labels, num_classes=n_classes)
            dice, _ = compute_dice(outputs, labels, num_classes=n_classes)
            pixel = compute_pixel_accuracy(outputs, labels)
            map50, class_map50 = compute_map50(outputs, labels, num_classes=n_classes)

            iou_scores.append(iou)
            dice_scores.append(dice)
            pixel_accs.append(pixel)
            map50_scores.append(map50)
            all_class_map50.append(class_map50)

    print("\n===== FINAL RESULTS =====")
    print(f"Mean IoU   : {np.mean(iou_scores):.4f}")
    print(f"mAP@0.5   : {np.mean(map50_scores):.4f}")
    print(f"Pixel Acc : {np.mean(pixel_accs):.4f}")

if __name__ == "__main__":
    main()