DEFAULT_VALUE_MAP = {
    0: 0,
    100: 1,
    200: 2,
    300: 3,
    500: 4,
    550: 5,
    700: 6,
    800: 7,
    7100: 8,
    10000: 9
}
value_map = DEFAULT_VALUE_MAP.copy()
mapping_mode = "int"

class_names = [
    'Background', 'Trees', 'Lush Bushes', 'Dry Grass', 'Dry Bushes',
    'Ground Clutter', 'Logs', 'Rocks', 'Landscape', 'Sky'
]

n_classes = len(value_map)

def build_color_palette(num_classes):
    base = np.array([
        [0, 0, 0],
        [34, 139, 34],
        [0, 255, 0],
        [210, 180, 140],
        [139, 90, 43],
        [128, 128, 0],
        [139, 69, 19],
        [128, 128, 128],
        [160, 82, 45],
        [135, 206, 235],
    ], dtype=np.uint8)

    if num_classes <= len(base):
        return base[:num_classes]

    rng = np.random.default_rng(42)
    extra = rng.integers(0, 255, size=(num_classes - len(base), 3), dtype=np.uint8)
    return np.vstack([base, extra])


color_palette = build_color_palette(n_classes)


def load_value_map(mapping_json_path):
    """Load mapping JSON generated by generate_mask_mapping.py."""
    with open(mapping_json_path, "r", encoding="utf-8") as f:
        data = json.load(f)

    mode = data.get("mode", "int")
    raw_mapping = data.get("mapping", {})
    parsed = {}

    if mode == "rgb":
        for key, value in raw_mapping.items():
            rgb = tuple(int(x) for x in str(key).split(","))
            if len(rgb) != 3:
                raise ValueError(f"Invalid RGB key in mapping: {key}")
            parsed[rgb] = int(value)
    else:
        for key, value in raw_mapping.items():
            parsed[int(key)] = int(value)

    if not parsed:
        raise ValueError("Loaded mapping is empty")

    return parsed, mode


def set_value_map(new_map, mode):
    """Update global mask mapping and derived class metadata."""
    global value_map, n_classes, mapping_mode, class_names, color_palette
    value_map = new_map
    n_classes = len(new_map)
    mapping_mode = mode
    class_names = [f"Class_{i}" for i in range(n_classes)]
    color_palette = build_color_palette(n_classes)

def convert_mask(mask):
    arr = np.array(mask)
    if mapping_mode == "rgb":
        if arr.ndim == 2:
            arr = np.stack([arr, arr, arr], axis=-1)
        if arr.shape[2] > 3:
            arr = arr[:, :, :3]

        new_arr = np.zeros(arr.shape[:2], dtype=np.uint8)
        for raw_value, new_value in value_map.items():
            rgb = np.array(raw_value, dtype=arr.dtype)
            new_arr[np.all(arr == rgb, axis=-1)] = new_value
    else:
        if arr.ndim == 3:
            arr = arr[:, :, 0]
        new_arr = np.zeros_like(arr, dtype=np.uint8)
        for raw_value, new_value in value_map.items():
            new_arr[arr == raw_value] = new_value

    return Image.fromarray(new_arr)

def mask_to_color(mask):
    h, w = mask.shape
    color_mask = np.zeros((h, w, 3), dtype=np.uint8)
    for class_id in range(n_classes):
        color_mask[mask == class_id] = color_palette[class_id]
    return color_mask

