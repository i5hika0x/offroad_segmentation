def compute_iou(pred, target, num_classes=10):
    pred = torch.argmax(pred, dim=1)
    pred, target = pred.view(-1), target.view(-1)

    iou_per_class = []
    for class_id in range(num_classes):
        pred_inds = pred == class_id
        target_inds = target == class_id

        intersection = (pred_inds & target_inds).sum().float()
        union = (pred_inds | target_inds).sum().float()

        if union == 0:
            iou_per_class.append(np.nan)
        else:
            iou_per_class.append((intersection / union).cpu().numpy())

    return np.nanmean(iou_per_class), iou_per_class

def compute_dice(pred, target, num_classes=10, smooth=1e-6):
    pred = torch.argmax(pred, dim=1)
    pred, target = pred.view(-1), target.view(-1)

    dice_per_class = []
    for class_id in range(num_classes):
        pred_inds = pred == class_id
        target_inds = target == class_id

        intersection = (pred_inds & target_inds).sum().float()
        dice = (2 * intersection + smooth) / (
            pred_inds.sum().float() + target_inds.sum().float() + smooth
        )
        dice_per_class.append(dice.cpu().numpy())

    return np.mean(dice_per_class), dice_per_class

def compute_pixel_accuracy(pred, target):
    pred_classes = torch.argmax(pred, dim=1)
    return (pred_classes == target).float().mean().cpu().numpy()

# 🔹 mAP@0.5 ADDITION
def compute_map50(pred, target, num_classes=10, iou_threshold=0.5):
    pred_classes = torch.argmax(pred, dim=1)
    ap_per_class = []

    for class_id in range(num_classes):
        pred_mask = (pred_classes == class_id)
        gt_mask = (target == class_id)

        intersection = (pred_mask & gt_mask).sum().float()
        union = (pred_mask | gt_mask).sum().float()

        if union == 0:
            ap_per_class.append(np.nan)
            continue

        iou = intersection / union
        ap = 1.0 if iou >= iou_threshold else 0.0
        ap_per_class.append(ap)

    return np.nanmean(ap_per_class), ap_per_class

